package com.codeprogramming;

public class LengthConcatReplace {
    public static void main(String[] args){
        String name="My Name is Preeti";
        System.out.println("Length: " + name.length());
        System.out.println("Concatenation : " + name.concat(" Rawat"));
        System.out.println("Replace: "+ name.replace("Preeti","preeti"));

    }
}
