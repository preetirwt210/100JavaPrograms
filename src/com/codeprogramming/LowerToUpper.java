package com.codeprogramming;

import java.util.Locale;

public class LowerToUpper {
    public static void main(String[] args){
        String s1="preeti rawat";
        String s2="PREETI RAWAT";
        String lowerToUpper=s1.toUpperCase();
        String upperToLower=s2.toLowerCase();
        System.out.println("Lower to upper :" + lowerToUpper);
        System.out.println("upper to lower :" + upperToLower);
    }
}
